# Transformando texto em maiúsculas usando o método .upper()
texto = "um texto minusculo qualquer"
texto_maiusculo = texto.upper()

print("Texto original:", texto)
print("Texto em maiusculas:", texto_maiusculo)

# Trabalhando com listas e métodos da notação ponto
lista = [1, 3, 2]
print("\nLista original:", lista)

lista.append(4)
print("Lista apos adicionar um elemento:", lista)

# Outros exemplos de métodos úteis para listas:
lista.reverse()
print("Lista invertida:", lista)

lista.sort()
print("Lista ordenada:", lista)
